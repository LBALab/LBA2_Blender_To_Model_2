# Material handling functions

# This is probably not needed but here just in case
def srgb_to_linear(c):
    c = c / 255.0
    if c <= 0.04045:
        return c / 12.92
    return ((c + 0.055) / 1.055) ** 2.4

def get_palette_material(index):

    name = "DOS_PAL_%03d" % index

    mat = bpy.data.materials.get(name)
    if mat is None:
        mat = bpy.data.materials.new(name)
        mat.use_nodes = False
        #mat.diffuse_color = PALETTE[index]
        
        r, g, b = palette[index]
        mat.use_shadeless = True
        mat.diffuse_color = (
        srgb_to_linear(r),
        srgb_to_linear(g),
        srgb_to_linear(b),
        )

    return mat

def apply_color_to_selection(context, index):

    obj = context.active_object
    if not obj or obj.type != 'MESH':
        return

    mat = get_palette_material(index)

    # Make sure the material is in the object
    if mat.name not in obj.data.materials:
        obj.data.materials.append(mat)

    slot_index = obj.data.materials.find(mat.name)
    if slot_index == -1:
        slot_index = len(obj.data.materials) - 1

    # Assign only the selected faces
    if context.mode == 'EDIT_MESH':

        bm = bmesh.from_edit_mesh(obj.data)

        for f in bm.faces:
            if f.select:
                f.material_index = slot_index

        bmesh.update_edit_mesh(obj.data)


# Drawing functions

def draw_box(x, y, w, h, col):

    r, g, b = col
    
    bgl.glColor3f(r / 255, g / 255, b / 255)
    bgl.glBegin(bgl.GL_QUADS)
    bgl.glVertex2f(x, y)
    bgl.glVertex2f(x + w, y)
    bgl.glVertex2f(x + w, y - h)
    bgl.glVertex2f(x, y - h)
    bgl.glEnd()

    bgl.glColor3f(0.1, 0.1, 0.1)
    bgl.glBegin(bgl.GL_LINE_LOOP)
    bgl.glVertex2f(x, y)
    bgl.glVertex2f(x + w, y)
    bgl.glVertex2f(x + w, y - h)
    bgl.glVertex2f(x, y - h)
    bgl.glEnd()


def draw_callback(self, context):
    try:
        region = context.region
        if not region:
            return

        x0 = 10
        y0 = region.height - 10

        for i, c in enumerate(palette):

            cx = i % columns
            cy = i // columns

            x = x0 + cx * (box + pad)
            y = y0 - cy * (box + pad)

            draw_box(x, y, box, box, c)

            # highlight the selected color
            if i == selected_color:
                bgl.glColor3f(1.0, 1.0, 1.0)
                bgl.glBegin(bgl.GL_LINE_LOOP)
                bgl.glVertex2f(x - 1, y + 1)
                bgl.glVertex2f(x + box + 1, y + 1)
                bgl.glVertex2f(x + box + 1, y - box - 1)
                bgl.glVertex2f(x - 1, y - box - 1)
                bgl.glEnd()
        
        font_id = 0
        
        bgl.glColor4f(1.0, 1.0, 1.0, 1.0)
        blf.position(font_id, 10, y - 30, 0)
        blf.size(font_id, 20, 50)
        blf.draw(font_id, "AITD 1 Palette")
        blf.position(font_id, 10, y - 45, 0)
        blf.draw(font_id, "Selected Color Index: %d" % (selected_color))
        blf.position(font_id, 10, y - 60, 0)
        blf.draw(font_id, "Hit Alt-Z for shadeless view")
        blf.position(font_id, 10, y - 75, 0)
        blf.draw(font_id, "Hit Esc to close operator")

    except Exception as e:
        print("Draw error:", e)

# Mouse detection
def get_index_from_mouse(mx, my, region):

    x0 = 10
    y0 = region.height - 10

    for i in range(len(palette)):

        cx = i % columns
        cy = i // columns

        x = x0 + cx * (box + pad)
        y = y0 - cy * (box + pad)

        if (x <= mx <= x + box) and (y - box <= my <= y):
            return i

    return None


# Draw handler functions
def redraw_all_view3d():
    for window in bpy.context.window_manager.windows:
        for area in window.screen.areas:
            if area.type == 'VIEW_3D':
                area.tag_redraw()

def start_overlay(context):

    global draw_handle

    stop_overlay()

    draw_handle = bpy.types.SpaceView3D.draw_handler_add(
        draw_callback,
        (None, context),
        'WINDOW',
        'POST_PIXEL'
    )
    
    redraw_all_view3d()


def stop_overlay():

    global draw_handle

    if draw_handle is not None:
        try:
            bpy.types.SpaceView3D.draw_handler_remove(draw_handle, 'WINDOW')
        except:
            pass
        draw_handle = None
    
    redraw_all_view3d()


# Modal operator class
class VIEW3D_OT_palette_painter(bpy.types.Operator):
    bl_idname = "view3d.palette_painter"
    bl_label = "AITD Palette"

    def invoke(self, context, event):

        if context.area.type != 'VIEW_3D':
            return {'CANCELLED'}

        start_overlay(context)

        context.window_manager.modal_handler_add(self)
        return {'RUNNING_MODAL'}

    def modal(self, context, event):

        global selected_color
        
        # If pressed escape
        if event.type == 'ESC':
            stop_overlay()
            return {'CANCELLED'}
        
        # If pressed left mouse button
        if event.type == 'LEFTMOUSE' and event.value == 'PRESS':

            region = context.region

            idx = get_index_from_mouse(
                event.mouse_region_x,
                event.mouse_region_y,
                region
            )

            if idx is not None:

                selected_color = idx

                apply_color_to_selection(context, idx)

                print("Applied palette index:", idx)

                if context.area:
                    context.area.tag_redraw()

        return {'PASS_THROUGH'}


# Register handling functions
def register():
    bpy.utils.register_class(VIEW3D_OT_palette_painter)


def unregister():
    stop_overlay()
    bpy.utils.unregister_class(VIEW3D_OT_palette_painter)


if __name__ == "__main__":
    register()

    bpy.ops.view3d.palette_painter('INVOKE_DEFAULT')